local function tweenCharacterTo(plr, targetCFrame)
   if not (plr and plr.Character) then return end
   local char = plr.Character
   local hrp = char:FindFirstChild("HumanoidRootPart")
   if not hrp then return end
   
   local distance = (hrp.Position - targetCFrame.Position).Magnitude
   local tweenTime = distance / (state.TweenSpeed * 50)
   
   local tweenInfo = TweenInfo.new(tweenTime, Enum.EasingStyle.Linear, Enum.EasingDirection.InOut)
   local tween = TweenService:Create(hrp, tweenInfo, {CFrame = targetCFrame + Vector3.new(0, 2, 0)})
   
   tween:Play()
   tween.Completed:Wait()
end

local function safeTeleportCharacterTo(plr, targetCFrame)
   if not (plr and plr.Character) then return end
   local char = plr.Character
   pcall(function()
       if typeof(char.PivotTo) == "function" then
           char:PivotTo(targetCFrame + Vector3.new(0, 2, 0))
       else
           local primary = char.PrimaryPart or char:FindFirstChild("HumanoidRootPart")
           if primary then
               if typeof(char.SetPrimaryPartCFrame) == "function" and char.PrimaryPart then
                   char:SetPrimaryPartCFrame(targetCFrame + Vector3.new(0, 2, 0))
               else
                   primary.CFrame = targetCFrame + Vector3.new(0, 2, 0)
               end
           end
       end
       local hrp = char:FindFirstChild("HumanoidRootPart")
       if hrp and hrp:IsA("BasePart") then
           hrp.AssemblyLinearVelocity = Vector3.zero
           hrp.RotVelocity = Vector3.zero
       end
   end)
   task.wait(0.1)
end

local function WaitHRP(plr)
    local hrp = plr.Character and plr.Character:FindFirstChild("HumanoidRootPart")
    while not hrp do
        task.wait(0.1)
        hrp = plr.Character and plr.Character:FindFirstChild("HumanoidRootPart")
    end
    return hrp
end

-- Global variables
local isTeleporting = false

-- Improved topos function with proper parameter handling
local function topos(plr, targetCFrame)
    if not plr then
        plr = game.Players.LocalPlayer
    end
    
    if not (plr.Character and plr.Character:FindFirstChild("HumanoidRootPart")) then
        return
    end
    
    -- Fix: Use targetCFrame instead of undefined 'Pos'
    local Distance = (targetCFrame.Position - plr.Character.HumanoidRootPart.Position).Magnitude
    
    if not targetCFrame then
        return
    end
    
    -- Create PartTele if it doesn't exist
    if not plr.Character:FindFirstChild("PartTele") then
        local PartTele = Instance.new("Part", plr.Character)
        PartTele.Size = Vector3.new(10, 1, 10)
        PartTele.Name = "PartTele"
        PartTele.Anchored = true
        PartTele.Transparency = 1
        PartTele.CanCollide = false -- Changed to false to prevent collision issues
        PartTele.CFrame = WaitHRP(plr).CFrame 
        
        PartTele:GetPropertyChangedSignal("CFrame"):Connect(function()
            if not isTeleporting then return end
            task.wait()
            if plr.Character and plr.Character:FindFirstChild("HumanoidRootPart") then
                WaitHRP(plr).CFrame = PartTele.CFrame
            end
        end)
    end
    
    isTeleporting = true
    
    -- Create tween with proper speed calculation using state.TweenSpeed
    local tweenSpeed = math.max(Distance / (state.TweenSpeed * 50), 0.1) -- Minimum tween time of 0.1 seconds
    local Tween = game:GetService("TweenService"):Create(
        plr.Character.PartTele, 
        TweenInfo.new(tweenSpeed, Enum.EasingStyle.Linear), 
        {CFrame = targetCFrame + Vector3.new(0, 2, 0)} -- Added offset like original function
    )
    
    Tween:Play()
    
    -- Wait for tween completion
    Tween.Completed:Connect(function(status)
        if status == Enum.PlaybackState.Completed then
            if plr.Character:FindFirstChild("PartTele") then
                plr.Character.PartTele:Destroy()
            end
            isTeleporting = false
        end
    end)
    
    -- Wait for completion
    Tween.Completed:Wait()
end

-- == Fixed Player Finding Function ==
local function findPlayerByName(searchName)
   if not searchName or searchName == "" then return nil end
   local lowerSearch = searchName:lower()
   
   -- First try exact username match
   for _, plr in ipairs(Players:GetPlayers()) do
       if plr.Name:lower() == lowerSearch then
           return plr
       end
   end
   
   -- Then try display name match
   for _, plr in ipairs(Players:GetPlayers()) do
       if plr.DisplayName:lower() == lowerSearch then
           return plr
       end
   end
   
   -- Finally try partial matches
   for _, plr in ipairs(Players:GetPlayers()) do
       if plr.Name:lower():find(lowerSearch) or plr.DisplayName:lower():find(lowerSearch) then
           return plr
       end
   end
   
   return nil
end

